/*04) Crie uma função que irá receber dois valores, o dividendo e o divisor. A função deverá imprimir o resultado
e o resto da divisão destes dois valores. */

function Q4(x,y)  {
    div = x/y
    restoDiv = x%y
    return `'Divisão: ${div}  Resto Da Divisão: ${restoDiv}'`
}

console.log(Q4(2,2))



